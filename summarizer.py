from google import genai

def summarize_news(news_text, api_key):
    # 최신 google-genai 패키지 문법 적용
    client = genai.Client(api_key=api_key)
    
    from datetime import datetime
    import pytz
    
    kst = pytz.timezone('Asia/Seoul')
    today_str = datetime.now(kst).strftime("%Y년 %m월 %d일 %A")

    prompt = f"""
[할루시네이션(환각) 절대 금지]
하루 2번 텔레그램으로 발송되는 뉴스 브리핑입니다. 제공된 팩트(스크래핑된 뉴스 데이터)에 기반해서만 작성하세요. 조금이라도 불확실하거나 없는 내용은 절대 지어내지 마세요.

다음은 방금 스크래핑한 각 분야별 주요 뉴스 헤드라인과 날씨 정보입니다.
이를 바탕으로 아래 양식에 맞추어 오늘 주요 뉴스 브리핑을 작성해주세요. 
사용자가 읽기 편하고 자연스러운 문장 ('~습니다/니다')으로 작성해야 합니다.

[중요 요청사항 - 가독성 및 진입 종목 추천 형식]
1. 각 뉴스의 내용이 다르다면 문장을 이어서 작성하지 말고, 개별 문장들을 가독성 있게 줄바꿈(bullet point 등)으로 끊어서 명확하게 분리해 작성해주세요. 이 규칙은 모든 섹터에 적용됩니다.
2. 각 분야별(경제, 증권금융, IT과학, 국제 등) 뉴스를 요약한 뒤, 해당 이슈로 인해 주목받을 수 있는 관련 주식을 **국내 주식 5개 이상, 해외 주식 5개 이상 (총 10개 이상)** 추천해주세요.
3. 종목을 추천할 때, 이미 주가가 많이 오른 유명한 **[대장주]** 뿐만 아니라, 뉴스 맥락상 앞으로 수혜를 입을 가능성이 높은 상대적 가벼운 종목인 **[저평가/수혜주]**를 함께 섞어서 추천해주세요.
4. 각 종목 이름에는 이것이 [대장주]인지 [저평가/수혜주]인지 형태를 시각적으로 명확하게 구분지어 주세요.

[주식 추천 표기 규칙]
1. 추천 주식은 [관련 국내 주식 5선]과 [관련 해외 주식 5선]으로 목록을 완전히 분리해서 작성해주세요.
2. 종목 번호 바로 뒤에 '주식 이름'이 가장 먼저 확실하게 나오도록 작성해주세요.
3. 주식 이름 바로 옆의 소괄호() 안에 (시장/종목종류) 형식으로 표기해주세요. 예: (코스피/대장주) 또는 (나스닥/수혜주)
4. 그 바로 다음 줄에 하이픈(-)으로 시작하여 해당 주식이 어떤 테마인지, 왜 수혜를 받는지 구체적인 이유를 설명해주세요.

양식 예시: 
[{today_str}] 주요 뉴스 브리핑

<경 제>
• [경제 뉴스 1 요약]
• [경제 뉴스 2 요약]
• [경제 뉴스 3 요약]

💡 [관련 국내 주식 5선]
1. 삼성전자 (코스피/대장주)
- 국내 반도체 선두주자, 글로벌 기술 패권 경쟁 우위 기대
2. 한미반도체 (코스닥/저평가수혜주)
- 반도체 후공정 장비 공급 우위로 실적 개선 전망
... (총 5개)

💡 [관련 해외 주식 5선]
1. 엔비디아 (나스닥/대장주)
- 전 세계 AI 반도체 시장 점유율 1위, 강력한 모멘텀 확보
2. AMD (나스닥/저평가수혜주)
- 가성비 AI칩 수요 증가에 따른 반사이익 수혜 기대
... (총 5개)

(다른 섹터도 위 양식과 동일한 방식으로 작성. 단, 사회/날씨 등 주식과 직접 연관이 떨어지는 분야는 주식 추천을 생략해도 됩니다.)

---
스크래핑된 뉴스 데이터:
{news_text}
"""
    
    # Pro 모델은 무료 속도 제한(Rate Limit)이 매우 엄격하므로, 할당량이 큰 최신 Flash 모델 사용
    try:
        response = client.models.generate_content(
            model='gemini-2.5-flash',
            contents=prompt
        )
        return response.text
    except Exception as e:
        print(f"gemini-2.5-flash 모델 에러, 대체 모델로 재시도 중... Error: {e}")
        response = client.models.generate_content(
            model='gemini-flash-latest',
            contents=prompt
        )
        return response.text
